package com.shadowbattler.simulator.model.species;

public class Stats3 {
    private int atk;
    private int def;
    private int hp;

    public Stats3(int atk, int def, int hp) {
        this.atk = atk;
        this.def = def;
        this.hp = hp;
    }

    public int getAtk() {
        return atk;
    }

    public int getDef() {
        return def;
    }

    public int getHp() {
        return hp;
    }


}
